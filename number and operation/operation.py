# --------------------------
# -- Arithmetic Operators --
# --------------------------
# [+] Addition
# [-] Subtraction
# [*] Multiplication
# [/] Division
# [%] Modulus
# [**] Exponent
# [//] Floor Division
# --------------------------

# Addition
print(5+4)
print(-2+6)
print(1.2+2)
print(2.3+4.5)

# Subtraction
print(39-12)
print(-5-32)
print(-10--64)
print(2.1-9.6)

# Multiplication
print(2*5)
print(-5*43)
print(-3*-64)
print(2.1*9.6)

print(5 + 10 * 5)
print((5+10)*5)

# Division
print(10 / 2)
print(int(10/2))
print(-55 / 5)
print(3 + 15 / 3)
print((3 + 15) / 3)

# Modulus
print(43 % 2)
print(10 % 2)
print(10 % 3)
print(11 % 3)

# Exponent
print( 2 ** 5)
print( 5 ** 6)
print(3 ** 2)

# Floor Division
print(47 // 10)
print(100 // 10)
print(109 // 10)
