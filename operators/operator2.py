# ---------------------------------------
# -- Comparison (relational) Operators --
# ---------------------------------------
# [ == ] Equal
# [ != ] Not Equal
# [ > ] Greater Than
# [ < ] Less Than
# [ >= ] Greater Than Or Equal
# [ <= ] Less Than Or Equal
# ---------------------------------------

# Equal  == , Not Equal !=

print(1 == 1)
print(1 == 5)
print(1== 1.0)

print("*" * 50)

print(1 != 1)
print(1 != 5)
print(1 != 1.0)
print("*" * 50)

# ---------------------------------------

# Greater Than >  , Less Than <

print(10 > 10)
print(10 > 20)
print(10 > 10.0)
print(10 > 4)

print("*" * 50)

print(10 < 10)
print(10 < 20)
print(10 < 10.0)
print(10 < 4)

print("*" * 50)

# ---------------------------------------
# Greater Than Or Equal >= , Less Than Or Equal <=

print(10 >= 10)
print(10 >= 20)
print(10 >= 10.0)
print(10 >= 4)
print("*" * 50)

print(10 <= 10)
print(10 <= 20)
print(10 <= 10.0)
print(10 <= 4)

print("*" * 50)
