#----------------------------------
# type() function that return the data type
# all data in python ara object
#----------------------------------

print(type(10)) #int -> Integer
print(type(1000000)) #int -> Integer
print(type(-70)) #int -> Integer

print(type(10.1)) #float -> floating point number
print(type(1.3216587)) #float -> floating point number
print(type(-10000.1)) #float -> floating point number


print(type("Hello Python")) #str -> String

print(type([1,2,3,4,5])) #list -> list

print(type((1,2,3,4,5))) #tuple -> Tuple

print(type({'one':1,'two':2,'three':3,'four':4,'five':5})) #DICT ->Dictionary

print(type(2==2)) #bool -> boolean true or false
